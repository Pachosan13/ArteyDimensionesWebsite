#!/usr/bin/env node

/**
 * One-time setup: Creates Arte y Dimensiones custom fields in GHL.
 * Run once, saves field IDs to custom-field-ids.json.
 *
 * Usage: node scripts/setup-ghl-fields.js
 * Reads GHL_ARTEDIMENSIONES_KEY and GHL_ARTEDIMENSIONES_LOCATION from .env
 */

import { writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));

const GHL_BASE = 'https://services.leadconnectorhq.com';
const API_VERSION = '2021-07-28';
const API_KEY = process.env.GHL_ARTEDIMENSIONES_KEY || 'pit-3be0b8fe-86fa-4142-bd73-e1871794c86b';
const LOCATION_ID = process.env.GHL_ARTEDIMENSIONES_LOCATION || 'CbeRKdqS8gHimMxfW8wV';

const FIELDS_TO_CREATE = [
  {
    name: 'Tipo de Proyecto',
    dataType: 'SINGLE_OPTIONS',
    options: [
      'Oficinas Corporativas',
      'Desarrollo Comercial',
      'Proyecto Institucional',
      'Desarrollo Residencial',
      'Proyecto Mixto',
      'Otro'
    ]
  },
  { name: 'Detalles del Proyecto', dataType: 'LARGE_TEXT' },
  { name: 'Fuente del Lead', dataType: 'TEXT' },
  { name: 'Servicio Interes', dataType: 'TEXT' },
  { name: 'Pagina de Origen', dataType: 'TEXT' },
];

async function ghl(method, path, body = null) {
  const url = `${GHL_BASE}${path}`;
  const headers = {
    'Authorization': `Bearer ${API_KEY}`,
    'Version': API_VERSION,
    'Content-Type': 'application/json',
    'Accept': 'application/json',
  };
  const opts = { method, headers };
  if (body) opts.body = JSON.stringify(body);

  const res = await fetch(url, opts);

  if (res.status === 429) {
    console.warn('  Rate limited — waiting 3s...');
    await new Promise(r => setTimeout(r, 3000));
    return ghl(method, path, body);
  }

  const text = await res.text();
  if (!res.ok) {
    throw new Error(`GHL ${method} ${path} → ${res.status}: ${text}`);
  }
  return JSON.parse(text);
}

async function main() {
  console.log('===================================================');
  console.log('SETUP: Creating Arte y Dimensiones custom fields');
  console.log(`Location: ${LOCATION_ID}`);
  console.log('===================================================\n');

  const fieldIds = {};

  for (const field of FIELDS_TO_CREATE) {
    const payload = {
      name: field.name,
      dataType: field.dataType,
    };

    if (field.options) {
      payload.options = field.options;
    }

    console.log(`Creating: ${field.name} (${field.dataType})...`);

    try {
      const result = await ghl(
        'POST',
        `/locations/${LOCATION_ID}/customFields`,
        payload
      );

      const id = result.customField?.id;
      const key = field.name
        .toLowerCase()
        .replace(/\s+/g, '_')
        .replace(/[^a-z0-9_]/g, '');

      fieldIds[key] = id;
      console.log(`  OK → ${id}`);
    } catch (err) {
      console.error(`  FAIL: ${err.message}`);
    }

    await new Promise(r => setTimeout(r, 600));
  }

  const output = {
    _comment: 'GHL Custom Field IDs — auto-generated by setup-ghl-fields.js',
    _location: `Arte y Dimensiones (${LOCATION_ID})`,
    _last_updated: new Date().toISOString(),
    ...fieldIds,
  };

  const outPath = join(__dirname, 'custom-field-ids.json');
  writeFileSync(outPath, JSON.stringify(output, null, 2) + '\n');
  console.log(`\nSaved field IDs to: ${outPath}`);
  console.log(JSON.stringify(output, null, 2));
}

main().catch(err => {
  console.error('FATAL:', err);
  process.exit(1);
});
